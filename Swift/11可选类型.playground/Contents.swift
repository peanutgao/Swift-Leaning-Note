import UIKit

/*
    > 为什么会引入 可选类型
 
    * 在OC开发中,如果一个变量暂时不用,可以赋值为0(基本数据类型), 或者赋值为空(对象类型)
    * 在Swift开发中, nil 是一种特殊的类型, 
    * 而Swift是强类型语言, 如果类型不匹配是不能直接赋值的, 所以可能会导致 不能给一个字符串类型的变量 赋值 nil
    * 如果 var name: String = nil 会出错
    * 为了解决类似的问题, 引入了 `可选类型`
 
 */

var name: String? = nil





















